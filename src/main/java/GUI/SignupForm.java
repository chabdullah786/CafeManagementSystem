    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
        // Save Button code here:
        User user = new User();
        user.setName(jTextField_name.getText());
        user.setEmail(jTextField_email.getText());
        user.setAddress(jTextField_adrs.getText());
        user.setMobileNumber(jTextField_phnNmbr.getText());
        user.setPassword(String.valueOf(jPasswordField_paswrd.getPassword()));
        user.setSecurityQuestion(jTextField_question.getText());
        user.setAnswer(jTextField_answer.getText());
        userDao.save(user);
        clear();
    }//GEN-LAST:event_SaveBtnActionPerformed

