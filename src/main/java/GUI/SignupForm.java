    public void clear() {

        jTextField_name.setText("");
        jTextField_email.setText("");
        jPasswordField_paswrd.setText("");
        jTextField_phnNmbr.setText("");
        jTextField_adrs.setText("");
        jTextField_question.setText("");
        jTextField_answer.setText("");
        SaveBtn.setEnabled(false);

    }

    public void validateFields() {
        String name = jTextField_name.getText();
        String email = jTextField_email.getText();
        String address = jTextField_adrs.getText();
        String mobileNumber = jTextField_phnNmbr.getText();
        String password = String.valueOf(jPasswordField_paswrd.getPassword());
        String securityQuestion = jTextField_question.getText();
        String answer = jTextField_answer.getText();

        if (!name.equals("") && email.matches(emailPattern) && mobileNumber.matches(mobileNoPattern) && mobileNumber.length() == 11 && !address.equals("") && !password.equals("") && !securityQuestion.equals("") && !answer.equals("")) {
            SaveBtn.setEnabled(true);
        } else {
            SaveBtn.setEnabled(false);
        }
    }
